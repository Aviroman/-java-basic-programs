int Addition(int num1, int num2)
{
    int Ret=0;
    
    Ret=num1+num2;
    
    return Ret;
}

int Subtraction(int num1, int num2)
{
    int Ret=0;
    
    Ret=num1-num2;
    
    return Ret;
}

int Multiplication(int num1, int num2)
{
    int Ret=0;
    
    Ret=num1*num2;
    
    return Ret;
}

int Division(int num1, int num2)
{
    int Ret=0;
    
    Ret=num1/num2;
    
    return Ret;
}

